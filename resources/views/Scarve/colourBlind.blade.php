                <div>
                </div><div>
                </div><div>Colour blind usually does not mean that people can not see any colours. The vast majority of people with colour blind see a range of colours, but they see some colours differently than others do. They may also have trouble telling the difference between certain colours or shades. Some very rare forms of colour blind make a person unable to see any colours.</div><br><div>
                </div><div>For most people, colour blind is inherited. That means it is passed down from the biological parents — from the mother or birthing parent in the most common red-green forms of colour blind. But people can also acquire colour vision deficiency later in life due to medical conditions or other reasons.</div><br><div>
